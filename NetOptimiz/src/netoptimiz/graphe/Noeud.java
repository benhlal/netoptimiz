package netoptimiz.graphe;

public class Noeud {

    private double abs;

    private double ord;

    private String nom;

    public Noeud () {
    }

    public double getAbs () {
        return abs;
    }

    public void setAbs (double val) {
        this.abs = val;
    }

    public String getNom () {
        return nom;
    }

    public void setNom (String val) {
        this.nom = val;
    }

    public double getOrd () {
        return ord;
    }

    public void setOrd (double val) {
        this.ord = val;
    }

}

